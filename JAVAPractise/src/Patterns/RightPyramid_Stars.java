package Patterns;

public class RightPyramid_Stars {

    /*
     *
     * *
     * * *
     * * * *
     * * * * *

     * */


    public static void main(String k[]){

        int n=5;

        for(int i=0; i<n; i++){
            System.out.print("here");
            for(int j=0; j<=i; j++){

                System.out.print("* ");
            }
            System.out.println();
        }


    }




}
